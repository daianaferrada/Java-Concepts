package Condicionales;

public class switc {

	public static void main(String[] args) {
		
	String colorluz = "rosa";
	
	switch (colorluz) {
	
	case "verde" :
		System.out.println("Puede continuar");
	break;
	case "rojo":
		System.out.println("Detente!");
	break;
	case "amarillo":
		System.out.println ("Cuidado");
	break;
	
	default:
		System.out.println ("Color invalido");
	}
	
		
		
		
	}

}
